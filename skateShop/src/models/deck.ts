export class Deck {

    length: number;
    deckColor: string;
    material: string;
    deckPrice: number;

constructor(length: number, deckColor: string, material: string, deckPrice: number) {

    this.length = length;
    this.deckColor = deckColor;
    this.material = material;
    deckPrice = deckPrice;
}

}